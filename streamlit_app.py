        return f"‚ùå Error de conexi√≥n: {str(e)}\n\n**Consejos generales:**\n‚Ä¢ Mant√©n la calma y escucha\n‚Ä¢ Haz preguntas abiertas\n‚Ä¢ Enf√≥cate en {prioridades[0] if prioridades else 'resolver necesidades'}"

